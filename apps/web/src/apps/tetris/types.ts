export type GameStatus = 'idle' | 'playing' | 'paused' | 'lost';

export type PieceType = 'I' | 'O' | 'T' | 'S' | 'Z' | 'J' | 'L';

export type Position = {
  x: number;
  y: number;
};

export type Piece = {
  type: PieceType;
  position: Position;
  rotation: number;
};

export type CellValue = PieceType | null;

export type GameState = {
  board: CellValue[][];
  currentPiece: Piece | null;
  nextPiece: PieceType;
  holdPiece: PieceType | null;
  canHold: boolean;
  score: number;
  level: number;
  lines: number;
  status: GameStatus;
};

